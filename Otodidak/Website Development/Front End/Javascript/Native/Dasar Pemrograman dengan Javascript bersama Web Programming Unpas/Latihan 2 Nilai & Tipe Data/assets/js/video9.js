// Angka Dalam Javascript

// tanpa desimal
let angka = 10
console.log(angka);

// dengan desimal
angka = 0.2
console.log(angka);

// dengan desimal
angka = 0.2
console.log(angka);

// Eksponen
angka = 123e5
let angka2 = 123e-5
console.log(angka);
console.log(angka2);

// Negatif
angka = -2
console.log(angka);

// Oktal
angka = 022
console.log(angka);

// HexaDesimal
angka = 0xFF
console.log(angka);

// Special Number
// Infinity & -Infinity
angka = Infinity // 2/0
angka2 = -Infinity // -2/0
console.log(angka);
console.log(angka2);

// Special Number
// NaN
angka = NaN // 2/0
console.log(angka);
