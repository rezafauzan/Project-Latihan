// Operator Logika
console.log('Operator Logika')

console.log('hasil dari (x > y) && (x%2) adalah' + ( (x > y) && (x%2) ) )
console.log('hasil dari (x > y) || (x%2) adalah' + ( (x > y) || (x%2) ) )
console.log('hasil dari !(x > y) adalah' + !(x > y) )

// Operator String ( Concatenate + )
console.log("Operator String ( Concatenate (+) )")
console.log("hasil dari string \"Nama : \" + \"Reza\" adalah Nama : " + "Reza") 
console.log("hasil dari string \"10 \" + 10 + 10 adalah" + "10" + 10 + 10) 

//  Operator TypeOf(operand)
console.log('Operator typeof()')
console.log('hasil dari typeof(10) adalah' + typeof(10))
console.log('hasil dari typeof(true) adalah' + typeof(true))
console.log('hasil dari typeof("Reza") adalah' + typeof("Reza"))

